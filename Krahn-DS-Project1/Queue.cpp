#include "Queue.h"

//----------------------------------------------------------------
// queue constructor
//----------------------------------------------------------------
Queue::Queue()
{
	front = rear = size = 0;
}

//----------------------------------------------------------------
// adds item to queue
//----------------------------------------------------------------
void Queue::Enqueue(QueueType value)
{
	theQueue[rear++] = value;
	size++;
}

//----------------------------------------------------------------
// removes item from queue and returns it
//----------------------------------------------------------------
QueueType Queue::Dequeue()
{
	QueueType returnValue = theQueue[front];

	for (int i = 0; i < rear - 1; i++)
	{
		theQueue[i] = theQueue[i + 1];
	}
	rear--;
	size--;

	return returnValue;
}

//----------------------------------------------------------------
// checks if queue is empty
//----------------------------------------------------------------
bool Queue::IsEmpty()
{
	return front == rear;
}

//----------------------------------------------------------------
// checks if queue is full
//----------------------------------------------------------------
bool Queue::IsFull()
{
	return rear == MAX_SIZE;
}

//----------------------------------------------------------------
// empties queue
//----------------------------------------------------------------
void Queue::MakeEmpty()
{
	front = rear = 0;
}

//----------------------------------------------------------------
// returns the size of the queue
//----------------------------------------------------------------
int Queue::Size()
{
	return size;
}

