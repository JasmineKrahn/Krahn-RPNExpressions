#include "Stack.h"

//----------------------------------------------------------------
// stack constructor
//----------------------------------------------------------------
Stack::Stack()
{
	top = -1;
	size = 0;
}

//----------------------------------------------------------------
// adds item to stack
//----------------------------------------------------------------
void Stack::Push(StackType value)
{
	theStack[++top] = value;
	size++;
}

//----------------------------------------------------------------
// removes item from stack and returns it
//----------------------------------------------------------------
StackType Stack::Pop()
{
	size--;
	return theStack[top--];	
}

//----------------------------------------------------------------
// returns last item added to stack
//----------------------------------------------------------------
StackType Stack::Top()
{
	return theStack[top];
}

//----------------------------------------------------------------
// checks if stack is empty
//----------------------------------------------------------------
bool Stack::IsEmpty()
{
	return top == -1;
}

//----------------------------------------------------------------
// checks if stack is full
//----------------------------------------------------------------
bool Stack::IsFull()
{
	return top == MAX_SIZE;
}

//----------------------------------------------------------------
// returns the size of the stack
//----------------------------------------------------------------
int Stack::Size()
{
	return size;
}

